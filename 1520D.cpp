#include <bits/stdc++.h>
using namespace std;

typedef int long long ll;
#define printnl(a) cout << a << "\n"
#define printab(a, b) cout << a << " " << b << "\n"
#define readV(vec) for (auto &e: vec) cin >> e;
#define debugV(vec) for (auto &e: vec) cout << e << " "; cout << endl;

void solve() {
    ll n;
    cin >> n;
    vector <ll> arr(n);
    readV(arr);
}

int main() {
    ll t;
    cin >> t;

    while (t--) solve();
}